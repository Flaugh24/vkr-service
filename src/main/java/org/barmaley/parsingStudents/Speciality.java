public class Speciality {
    int id;
    String code;
    String name;
    int generation;
    String level_edu;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getGeneration() {
        return generation;
    }

    public void setGeneration(int generation) {
        this.generation = generation;
    }

    public String getLevel_edu() {
        return level_edu;
    }

    public void setLevel_edu(String level_edu) {
        this.level_edu = level_edu;
    }
}
